package module1programs;

public class Switchstatement {

	public static void main(String[] args) {
		switch(3)
		{
			case 1: System.out.println("Case1 is executed");
			break;
			case 2: System.out.println("Case2 is executed");
			break;
			case 3: System.out.println("Case3 is executed");
			break;
			case 4: System.out.println("Case4 is executed");
			break;
			case 5: System.out.println("Case5 is executed");
			break;
			default: System.out.println("No result found");
		}
	}

}
