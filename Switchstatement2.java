package module1programs;

public class Switchstatement2 {
	public static void main(String[] args) {
		String day="Tuesday";
		switch(day)
		{
		case "Monday":
			System.out.println("Start of the week!");
			break;
		case "Tuesday":
			System.out.println("Mid of the week!");
			break;
		case "Wednesday":
			System.out.println("End of the week!");
			break;
		}
	}

}
