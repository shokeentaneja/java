package module1programs;
// program to define Initailization and declaration and utilization
public class AssignmentOperators {
	public static void main(String[] args) {
		
		int age;		//Initailization
		age=20;			//declaration
		System.out.println(age);
		
		byte a=100;		//Initailization and declaration in a same line
		System.out.println(a); //utilization
		
		short b=2500;		//Initailization and declaration in a same line
		System.out.println(b);
		
		long c=1878687670l;		//Initailization and declaration in a same line
		System.out.println(c);
		
		double pi=3.14;		//Initailization and declaration in a same line
		System.out.println(pi);
		
		float pivalue=3.14f;		//Initailization and declaration in a same line
		System.out.println(pivalue);
		
		char gender='M';		//Initailization and declaration in a same line
		System.out.println(gender);
		
		String name="Ram";
		System.out.println(name);
		
		boolean answer=false;
		System.out.println(answer);
		
		
		
	}

}
