package module1programs;

public class Elseifstatement {

	public static void main(String[] args) {
		int a=20,b=10,c=40;
		
		if(a==11) 
		{
			System.out.println("1");
		}
		
		else if(a>=b)
		{
			System.out.println("2");
		}
		
		else if(b<=c)
		{
			System.out.println("3");
		}
		
		else
		{
			System.out.println("No result found");
		}
	}
		

}
